// our main reducer
const rootReducer = (state = initState, action) => {
  // action types management
  if (action.type === 'DELETE_POST')   {
    // using fileter to get new array of posts wihout deleted post id
    let newPosts = state.posts.filter((post) => {
      return action.id !== post.id;
    })
    // return new object representing our state
    return {
      ...state, // copy current state with spread
      posts: newPosts // rewrite posts in state (store)
      // no direct manipulation because comp can't receive redux store state directly
    }
  }

// return state for Redux store
  return state;
}

export default rootReducer